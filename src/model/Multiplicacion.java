package model;

public class Multiplicacion {

	public double num1;
	public double num2;

	public Multiplicacion(double num1, double num2) {
		this.num1 = num1;
		this.num2 = num2;
	}
	
	/**
	 * Este método te permitirá hacer restas Gozu...!!!
	 * */
	public Double multplicar() {

		return num1 * num2;
	}
}
